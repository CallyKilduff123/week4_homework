# as there is no comma the variable of tup is read as a string
tup = 'Hello'
print(type(tup))
# when the length is worked out it is looking at all the characters in the string
print(len(tup))

# the comma denotes the variable of tup being a tuple
# when the length of the tuple is worked out it is looking at the number of items in the tuple
tup = 'Hello',
print(type(tup))
print(len(tup))